class SimulationEngine:
    """
    Real RTL simulation executor.

    Workflow:
        1. Write RTL + testbench to an isolated temp directory.
        2. Generate a Makefile via MakefileGenerator.
        3. Run `make` (Verilator + cocotb) with an async subprocess.
        4. Parse VCD → real CoverageMetrics.
        5. Parse stderr → SimulationFailure list.
        6. Clean up temp directory.
        7. Return a fully populated SimulationResult.
    """

    DEFAULT_TIMEOUT = 300       # 5 minutes per simulation run
    MAX_RETRIES     = 3

    def __init__(
        self,
        timeout_sec: int = DEFAULT_TIMEOUT,
        max_retries: int = MAX_RETRIES,
        work_base_dir: Optional[Path] = None,
    ):
        self.timeout_sec   = timeout_sec
        self.max_retries   = max_retries
        self.work_base_dir = work_base_dir or Path(tempfile.gettempdir()) / "sim_engine"
        self.work_base_dir.mkdir(parents=True, exist_ok=True)

        self._makefile_gen = MakefileGenerator()
        self._vcd_parser   = VCDCoverageParser()
        self._assert_parser = AssertionParser()

        # Verify Verilator is installed once at startup
        self._verilator_available = self._check_tool("verilator")
        self._cocotb_available    = self._check_tool("cocotb-config")

        if not self._verilator_available:
            logger.warning("Verilator not found — simulations will not run. "
                           "Install: apt-get install verilator")
        if not self._cocotb_available:
            logger.warning("cocotb not found — testbenches will not run. "
                           "Install: pip install cocotb")

    # ── Public API ────────────────────────────────────────────────────────

    async def run_simulation(
        self,
        rtl_files: List[str],
        testbench_module: str,
        top_module: str,
        extra_defines: Optional[Dict[str, str]] = None,
    ) -> SimulationResult:
        """
        Run a full Verilator + cocotb simulation with retry and timeout.

        Args:
            rtl_files:        Paths to .v / .sv RTL files.
            testbench_module: Python module name of the cocotb test (no .py).
            top_module:       Top-level Verilog module name.
            extra_defines:    Optional Verilog `define values.

        Returns:
            SimulationResult with real coverage, failures, and logs.
        """
        if not self._verilator_available or not self._cocotb_available:
            logger.error("Cannot run simulation — tools not available.")
            return SimulationResult(
                success  = False,
                coverage = CoverageMetrics(),
                error    = "Verilator or cocotb not installed.",
            )

        attempt = 0
        last_result: Optional[SimulationResult] = None

        while attempt < self.max_retries:
            attempt += 1
            logger.info(f"Simulation attempt {attempt}/{self.max_retries} "
                        f"for top_module='{top_module}'")
            work_dir = self._create_work_dir(top_module)
            try:
                last_result = await self._run_once(
                    rtl_files, testbench_module, top_module,
                    extra_defines, work_dir
                )
                if last_result.success or last_result.timed_out:
                    break   # Don't retry on clean success or hard timeout
            except Exception as e:
                logger.error(f"Attempt {attempt} raised exception: {e}", exc_info=True)
                last_result = SimulationResult(
                    success=False, coverage=CoverageMetrics(), error=str(e)
                )
            finally:
                self._cleanup_work_dir(work_dir)

            if attempt < self.max_retries:
                backoff = 2 ** attempt
                logger.info(f"Retrying in {backoff}s…")
                await asyncio.sleep(backoff)

        return last_result or SimulationResult(
            success=False, coverage=CoverageMetrics(), error="All retries exhausted."
        )

    async def lint_only(self, rtl_files: List[str]) -> Dict[str, Any]:
        """
        Quick Verilator lint check without running a simulation.
        Returns {"passed": bool, "warnings": [...], "errors": [...]}.
        """
        warnings, errors = [], []

        for rtl_file in rtl_files:
            if not Path(rtl_file).exists():
                errors.append(f"File not found: {rtl_file}")
                continue
            try:
                proc = await asyncio.create_subprocess_exec(
                    "verilator", "--lint-only", rtl_file,
                    stdout=asyncio.subprocess.PIPE,
                    stderr=asyncio.subprocess.PIPE,
                )
                _, stderr = await asyncio.wait_for(
                    proc.communicate(), timeout=30
                )
                text = stderr.decode(errors="replace")
                warnings += re.findall(r'%Warning.*', text)
                errors   += re.findall(r'%Error.*', text)
            except asyncio.TimeoutError:
                errors.append(f"Lint timeout: {rtl_file}")
            except Exception as e:
                errors.append(f"Lint failed ({rtl_file}): {e}")

        return {
            "passed":   len(errors) == 0,
            "warnings": warnings,
            "errors":   errors,
        }

    # ── Internal helpers ──────────────────────────────────────────────────

    async def _run_once(
        self,
        rtl_files: List[str],
        testbench_module: str,
        top_module: str,
        extra_defines: Optional[Dict[str, str]],
        work_dir: Path,
    ) -> SimulationResult:
        import time
        start = time.monotonic()

        # 1. Copy RTL files into the work directory (keeps paths clean)
        staged_rtl = self._stage_files(rtl_files, work_dir)

        # 2. Generate Makefile
        makefile_path = self._makefile_gen.generate(
            rtl_files      = staged_rtl,
            testbench_module = testbench_module,
            top_module     = top_module,
            work_dir       = work_dir,
            extra_defines  = extra_defines,
        )

        # 3. Execute make
        timed_out = False
        try:
            proc = await asyncio.create_subprocess_exec(
                "make", "-f", str(makefile_path),
                cwd   = str(work_dir),
                stdout = asyncio.subprocess.PIPE,
                stderr = asyncio.subprocess.PIPE,
                env   = {**os.environ, "COCOTB_RESOLVE_X": "ZEROS"},
            )
            stdout_bytes, stderr_bytes = await asyncio.wait_for(
                proc.communicate(),
                timeout = self.timeout_sec
            )
            stdout = stdout_bytes.decode(errors="replace")
            stderr = stderr_bytes.decode(errors="replace")
            exit_code = proc.returncode

        except asyncio.TimeoutError:
            logger.warning(
                f"Simulation timed out after {self.timeout_sec}s "
                f"(possible livelock in '{top_module}')"
            )
            try:
                proc.kill()
            except Exception:
                pass
            return SimulationResult(
                success   = False,
                coverage  = CoverageMetrics(),
                timed_out = True,
                error     = f"Timed out after {self.timeout_sec}s — suspected livelock.",
                elapsed_sec = self.timeout_sec,
            )

        elapsed = time.monotonic() - start

        # 4. Parse VCD for coverage
        vcd_path = work_dir / "sim.vcd"
        if not vcd_path.exists():
            # Verilator also generates dump.vcd in some configs
            vcd_path = next(work_dir.glob("*.vcd"), None)

        coverage = (
            self._vcd_parser.parse(str(vcd_path))
            if vcd_path and vcd_path.exists()
            else CoverageMetrics()
        )

        # 5. Parse assertion failures from stderr
        failures = self._assert_parser.parse(stderr)

        # 6. Log summary
        logger.info(
            f"Simulation done in {elapsed:.1f}s — "
            f"exit={exit_code}, bugs={len(failures)}, "
            f"coverage={coverage.overall():.1f}%"
        )
        if stderr:
            logger.debug(f"Stderr snippet:\n{stderr[:500]}")

        return SimulationResult(
            success      = (exit_code == 0),
            coverage     = coverage,
            failures     = failures,
            waveform_path = str(vcd_path) if (vcd_path and vcd_path.exists()) else None,
            log          = stdout,
            stderr       = stderr,
            elapsed_sec  = elapsed,
        )

    def _stage_files(self, rtl_files: List[str], work_dir: Path) -> List[str]:
        """Copy RTL files into the work directory and return their new paths."""
        staged = []
        for src in rtl_files:
            src_path = Path(src)
            if not src_path.exists():
                logger.warning(f"RTL file not found, skipping: {src}")
                continue
            dest = work_dir / src_path.name
            shutil.copy2(src_path, dest)
            staged.append(str(dest))
        return staged

    def _create_work_dir(self, top_module: str) -> Path:
        ts = datetime.now().strftime("%Y%m%d_%H%M%S_%f")
        work_dir = self.work_base_dir / f"{top_module}_{ts}"
        work_dir.mkdir(parents=True, exist_ok=True)
        return work_dir

    def _cleanup_work_dir(self, work_dir: Path) -> None:
        try:
            shutil.rmtree(work_dir, ignore_errors=True)
            logger.debug(f"Cleaned up work dir: {work_dir}")
        except Exception as e:
            logger.warning(f"Could not clean up {work_dir}: {e}")

    @staticmethod
    def _check_tool(tool: str) -> bool:
        return shutil.which(tool) is not None