@dataclass
class CoverageMetrics:
    line_coverage: float = 0.0
    toggle_coverage: float = 0.0
    branch_coverage: float = 0.0
    functional_coverage: float = 0.0

    def overall(self) -> float:
        """Weighted average of all coverage types."""
        return (
            self.line_coverage      * 0.35 +
            self.toggle_coverage    * 0.25 +
            self.branch_coverage    * 0.25 +
            self.functional_coverage * 0.15
        )

    def to_dict(self) -> Dict[str, float]:
        return {
            "line":        round(self.line_coverage, 2),
            "toggle":      round(self.toggle_coverage, 2),
            "branch":      round(self.branch_coverage, 2),
            "functional":  round(self.functional_coverage, 2),
            "overall":     round(self.overall(), 2),
        }


@dataclass
class SimulationFailure:
    assertion_name: str
    time_ns: float
    signal: str
    expected: str
    actual: str
    file: str
    line: int

    def to_dict(self) -> Dict[str, Any]:
        return {
            "assertion":  self.assertion_name,
            "time_ns":    self.time_ns,
            "signal":     self.signal,
            "expected":   self.expected,
            "actual":     self.actual,
            "location":   f"{self.file}:{self.line}",
        }


@dataclass
class SimulationResult:
    success: bool
    coverage: CoverageMetrics
    failures: List[SimulationFailure] = field(default_factory=list)
    waveform_path: Optional[str] = None
    log: str = ""
    stderr: str = ""
    elapsed_sec: float = 0.0
    timed_out: bool = False
    error: Optional[str] = None

    def to_dict(self) -> Dict[str, Any]:
        return {
            "success":      self.success,
            "coverage":     self.coverage.to_dict(),
            "failures":     [f.to_dict() for f in self.failures],
            "waveform":     self.waveform_path,
            "elapsed_sec":  round(self.elapsed_sec, 2),
            "timed_out":    self.timed_out,
            "error":        self.error,
            "bugs_found":   len(self.failures),
        }


