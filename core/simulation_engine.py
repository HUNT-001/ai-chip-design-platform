class AssertionParser:
    """Parses Verilator/cocotb stderr for assertion failures."""

    # Patterns for common assertion failure formats
    _PATTERNS = [
        # Verilator SVA: %Error: file.sv:42: Assertion failed
        re.compile(
            r'%Error.*?(\S+\.sv):(\d+).*?Assertion\s+(?:failed|violation)',
            re.IGNORECASE
        ),
        # cocotb: AssertionError at time ...
        re.compile(
            r'AssertionError.*?time\s+([\d.]+)\s*ns',
            re.IGNORECASE
        ),
        # Verilator coverage assertion
        re.compile(
            r'\[(\d+)\s*ns\].*?ASSERT\s+FAIL:\s*(\w+)\s+exp=(\S+)\s+got=(\S+)',
            re.IGNORECASE
        ),
    ]

    def parse(self, stderr: str) -> List[SimulationFailure]:
        failures: List[SimulationFailure] = []

        for line in stderr.splitlines():
            # Pattern 1: Verilator SVA
            m = self._PATTERNS[0].search(line)
            if m:
                failures.append(SimulationFailure(
                    assertion_name = "SVA_violation",
                    time_ns        = 0.0,
                    signal         = "unknown",
                    expected       = "pass",
                    actual         = "fail",
                    file           = m.group(1),
                    line           = int(m.group(2)),
                ))
                continue

            # Pattern 2: cocotb assertion
            m = self._PATTERNS[1].search(line)
            if m:
                failures.append(SimulationFailure(
                    assertion_name = "cocotb_assertion",
                    time_ns        = float(m.group(1)),
                    signal         = "unknown",
                    expected       = "pass",
                    actual         = "fail",
                    file           = "testbench",
                    line           = 0,
                ))
                continue

            # Pattern 3: Explicit assert with exp/got
            m = self._PATTERNS[2].search(line)
            if m:
                failures.append(SimulationFailure(
                    assertion_name = m.group(2),
                    time_ns        = float(m.group(1)),
                    signal         = m.group(2),
                    expected       = m.group(3),
                    actual         = m.group(4),
                    file           = "rtl",
                    line           = 0,
                ))

        return failures


