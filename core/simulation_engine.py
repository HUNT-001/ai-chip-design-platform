class MakefileGenerator:
    """Generates Verilator-compatible Makefiles for cocotb simulations."""

    def generate(
        self,
        rtl_files: List[str],
        testbench_module: str,
        top_module: str,
        work_dir: Path,
        extra_defines: Optional[Dict[str, str]] = None,
    ) -> Path:
        """
        Write a Makefile into work_dir and return its path.

        Args:
            rtl_files:         List of absolute paths to .v / .sv files.
            testbench_module:  Python module name of the cocotb test (no .py).
            top_module:        Top-level Verilog module name.
            work_dir:          Directory where the Makefile will be written.
            extra_defines:     Optional dictionary of Verilog defines.
        """
        defines_str = " ".join(
            f"-D{k}={v}" for k, v in (extra_defines or {}).items()
        )
        verilog_sources = " ".join(str(f) for f in rtl_files)

        makefile_content = f"""# Auto-generated by SimulationEngine — do not edit manually
SIM        = verilator
TOPLEVEL   = {top_module}
MODULE     = {testbench_module}
VERILOG_SOURCES = {verilog_sources}
EXTRA_ARGS = --trace --coverage {defines_str}
COMPILE_ARGS = --timescale 1ns/1ps

include $(shell cocotb-config --makefiles)/Makefile.sim
"""
        makefile_path = work_dir / "Makefile"
        makefile_path.write_text(makefile_content, encoding="utf-8")
        logger.debug(f"Makefile written to {makefile_path}")
        return makefile_path


